//WAP TO CALCULATE THE AREA OF  TRIANGLE.(BY USING 2 VARIABLES)
#include<stdio.h>
int main()
{
    int b,h;
    /*
    b = base
    h = height
    */
    printf("THE BASE AND HEIGHT OF TRIANGLE\n");
    scanf("%d %d",&b,&h);
    printf("THE AREA OF TRIANGLE IS %.2f",0.5*b*h);
    return 0;
}