//WAP TO CALCULATE THE AREA OF RECTANGLE. (BY USING TWO VARIABLES)
#include<stdio.h>
int main()
{
    int l,b;
    /*
    l = length 
    b = breadth
    */ 
    printf("ENTER THE LENGTH AND BREADTH OF RECTANGLE\n");
    scanf("%d %d",&l,&b);
    printf("THE AREA OF RECTANGLE IS %d",l*b);

    return 0;
}